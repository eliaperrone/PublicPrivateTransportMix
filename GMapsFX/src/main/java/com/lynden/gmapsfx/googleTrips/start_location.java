package com.lynden.gmapsfx.googleTrips;

public class start_location {
    private float lat;
    private float lng;

    public float getLat() {
        return lat;
    }

    public float getLng() {
        return lng;
    }

    @Override
    public String toString() {
        return "start_location{" +
                "lat=" + lat +
                ", lng=" + lng +
                '}';
    }

}
